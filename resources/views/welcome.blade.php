<script>
    $(document).ready(function() {
        $('#otherEditor').hide();
            $('#blogEditor').hide();
    });

    $('#postType').on('change',function(){
        var postType = $('#postType').val();
        if(postType == 'post'){
            $('#postEditor').show();
            $('#otherEditor').hide();
            $('#blogEditor').hide();
        }
        if(postType == 'blog'){
            $('#blogEditor').show();
            $('#otherEditor').hide();
            $('#postEditor').hide();
        }
        if(postType == 'twitter' || postType == 'facebook' || postType == 'instagram' || postType == 'linkedin'){
            $('#postEditor').hide();
            $('#blogEditor').hide();
            $('#otherEditor').show();
        }
    });
</script>

